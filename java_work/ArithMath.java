
class ArithMath{
	public static void main(String[] args) {
		
		int x=10,y=5;

		int z = x+y;
		System.out.println(x+" + "+y+" = "+z); // 10+5 = 15
	
		z = x-y;
		System.out.println(x+" - "+y+" = "+z); // 10+5 = 15

		z = x*y;
		System.out.println(x+" * "+y+" = "+z); // 10+5 = 15

		x = 5;
		y = 7;
		z = x%y;
		System.out.println(x+" % "+y+" = "+z); // 10+5 = 15

		x = 5;
		y = 7;
		z = x/y;
		System.out.println(x+" / "+y+" = "+z); // 10+5 = 15

		//  5%7 , x/y
		// 6%2
	}
}